/*
    *** HELLO WORLD PROGRAM ***

    Program that prints "Hello world!" string on screen. It uses println! macro.

    Macro is an expression that has an '!' before the parenthesis, instead of calling function it expands into source code compiling with rest of the program.

    Main function:

    function notation
    v
    fn main() {} <- block of code
       ^
       function name
    
    Every instruction ends with ';'
*/

